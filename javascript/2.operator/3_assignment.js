/*
    대입 연산자(중첩 연산자) : +=, -=, *=, /=, ...
    연산자가 무조건 앞으로 와야 함. = 뒤에 나오면 에러.
*/

let a = 10;
console.log(a);

a += 10;    // a = a + 10 이랑 같음.
console.log(a);

a -= 10;    // a = a - 10 이랑 같음.
console.log(a); 

a *= 3;     // a = a * 3 이랑 같음.
console.log(a);

a /= 6;     // a = a / 6 이랑 같음.
console.log(a);